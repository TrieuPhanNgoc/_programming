#include "common_math.h"

double add(double a, double b)
{
    return a + b;
}

double sub(double a, double b)
{
    return a - b;
}

double mul(double a, double b)
{
    return a * b;
}

double div(double a, double b)
{
    return a / b;
}

double max(double a, double b)
{
    return max(a, b);
}

double min(double a, double b)
{
    return min(a, b);
}
